def _render_proof_runner(package_name: str) -> str:
    return f"""from __future__ import annotations

import argparse
from pathlib import Path

from custom_actions import ProofActions
from {package_name} import Parser

def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser()
    parser.add_argument("input_file", type=Path)
    return parser.parse_args()


def main() -> int:
    args = parse_args()
    input_file = args.input_file

    if not input_file.exists():
        raise FileNotFoundError(f"Missing input file: {{input_file}}")

    text = input_file.read_text(encoding=\"utf-8\")
    actions = ProofActions()
    parser = Parser(actions=actions)
    result = parser.parse(text)

    print(\"Parse result:\")
    print(actions.parse_result)

    print(f\"Accepted: {{result.accepted}}\")
    print(f\"Reductions: {{len(result.reductions)}}\")
    print(f\"Action callbacks: {{actions.reduction_count}}\")
    return 0


if __name__ == \"__main__\":
    raise SystemExit(main())
"""


def _to_snake_case(name: str) -> str:
    out: list[str] = []
    for i, ch in enumerate(name):
        if ch.isupper() and i > 0:
            out.append("_")
        out.append(ch.lower())
    return "".join(out)


def _detect_start_symbol_name(project_root: Path, export_file: str) -> str | None:
    export_path = Path(export_file)
    if not export_path.is_absolute():
        export_path = project_root / export_path

    if not export_path.exists():
        return None

    try:
        raw = load_json_file(export_path)
        model = parse_export_model(raw)
    except Exception:
        return None

    if not model.non_terminal_names:
        return None
    return model.non_terminal_names[model.start_symbol_index]


def _render_custom_actions(package_name: str, start_symbol_name: str | None) -> str:
    imports = [
        "from __future__ import annotations",
        "",
        f"from {package_name} import UserActions",
        f"from {package_name}.nodes import NonTerminalNode",
    ]

    method_lines: list[str] = []
    if start_symbol_name:
        start_type = f"{start_symbol_name}Node"
        imports.append(f"from {package_name}.nodes import {start_type}")
        method_lines = [
            "",
            f"    def on_{_to_snake_case(start_symbol_name)}(self, node: {start_type}):",
            "        self.parse_result = node",
            "        return node",
        ]

    lines = imports + [
        "",
        "",
        "class ProofActions(UserActions):",
        "    def __init__(self) -> None:",
        "        super().__init__()",
        "        self.reduction_count = 0",
        "        self.parse_result = None",
    ]
    lines.extend(method_lines)
    lines.extend(
        [
            "",
            "    def on_non_terminal(self, name: str, node: NonTerminalNode):",
            "        self.reduction_count += 1",
            "        return node",
        ]
    )

    return "\n".join(lines) + "\n"


def _render_gitignore(package_name: str) -> str:
    return f""".venv/
__pycache__/
*.pyc
{package_name}/
**/*.egg-info/
"""


def scaffold_project(
    out_dir: str | Path,
    project_name: str,
    package_name: str,
    export_file: str,
    force: bool = False,
) -> Path:
    root = Path(out_dir)
    if root.exists() and any(root.iterdir()) and not force:
        raise ValueError(f"Target directory is not empty: {root}. Use --force to overwrite files.")

    root.mkdir(parents=True, exist_ok=True)
    scripts_dir = root / "scripts"
    scripts_dir.mkdir(parents=True, exist_ok=True)
    start_symbol_name = _detect_start_symbol_name(root, export_file)

    files: dict[Path, str] = {
        root / "README.md": _render_readme(project_name, package_name, export_file),
        root / "pyproject.toml": _render_pyproject(project_name),
        root / ".gitignore": _render_gitignore(package_name),
        root / "proof_runner.py": _render_proof_runner(package_name),
        root / "custom_actions.py": _render_custom_actions(package_name, start_symbol_name),
        root / "sample.txt": "Var abc End\n",
        scripts_dir / "bootstrap.ps1": _render_bootstrap_ps1(),
        scripts_dir / "generate-parser.ps1": _render_generate_parser_ps1(package_name, export_file),
        scripts_dir / "run-proof.ps1": _render_run_proof_ps1(package_name, export_file),
    }

    for path, content in files.items():
        if path.exists() and not force:
            raise ValueError(f"Refusing to overwrite existing file: {path}. Use --force.")
        path.write_text(content, encoding="utf-8")

    return root
