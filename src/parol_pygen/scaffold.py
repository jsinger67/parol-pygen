def _ps_escape_single_quoted(value: str) -> str:
    return value.replace("'", "''")


def _render_bootstrap_ps1() -> str:
    return """$ErrorActionPreference = \"Stop\"

$root = Split-Path -Parent $PSScriptRoot
Push-Location $root
try {
    $uv = Get-Command uv -ErrorAction SilentlyContinue
    if (-not $uv) {
        throw \"uv is required but was not found in PATH. Install uv from https://docs.astral.sh/uv/getting-started/installation/.\"
    }

    $localPython = Join-Path $root \".venv\\Scripts\\python.exe\"

    if (-not (Test-Path $localPython)) {
        &uv venv .venv
        if ($LASTEXITCODE -ne 0 -or -not (Test-Path $localPython)) {
            throw \"Local virtual environment creation failed via uv.\"
        }
    }

    &uv sync
    if ($LASTEXITCODE -ne 0) {
        throw \"Failed to install project dependencies with uv.\"
    }
}
finally {
    Pop-Location
}
"""


def _render_generate_parser_ps1(package_name: str, export_file: str) -> str:
    package_name = _ps_escape_single_quoted(package_name)
    export_file = _ps_escape_single_quoted(export_file)
    return f"""$ErrorActionPreference = \"Stop\"

$root = Split-Path -Parent $PSScriptRoot
Push-Location $root
try {{
    $uv = Get-Command uv -ErrorAction SilentlyContinue
    if (-not $uv) {{
        throw \"uv is required but was not found in PATH. Install uv from https://docs.astral.sh/uv/getting-started/installation/.\"
    }}

    $pythonExe = Join-Path $root \".venv\\Scripts\\python.exe\"
    if (-not (Test-Path $pythonExe)) {{
        throw \"Missing local virtual environment. Run ./scripts/bootstrap.ps1 first.\"
    }}

    $exportFile = '{export_file}'
    if (-not (Test-Path $exportFile)) {{
        throw \"Export file not found: $exportFile\"
    }}

    & uv run --python $pythonExe -m parol_pygen.cli generate --export $exportFile --out . --package '{package_name}'
}}
finally {{
    Pop-Location
}}
"""


def _render_run_proof_ps1(package_name: str, export_file: str) -> str:
    package_name = _ps_escape_single_quoted(package_name)
    export_file = _ps_escape_single_quoted(export_file)
    return f"""$inputFile = $args[0]

$ErrorActionPreference = \"Stop\"

$root = Split-Path -Parent $PSScriptRoot
Push-Location $root
try {{
    $pythonExe = Join-Path $root \".venv\\Scripts\\python.exe\"
    if (-not (Test-Path $pythonExe)) {{
        throw \"Missing local virtual environment. Run ./scripts/bootstrap.ps1 first.\"
    }}

    if (-not (Test-Path '{package_name}')) {{
        & $PSScriptRoot\generate-parser.ps1
    }}


    if (-not $inputFile) {{
        throw \"Missing input file argument. Usage: ./scripts/run-proof.ps1 <path-to-source-file>\"
    }}

    & $pythonExe -m parol_pygen.cli validate --export '{export_file}'
    & $pythonExe .\proof_runner.py $inputFile
}}
finally {{
    Pop-Location
}}
"""


